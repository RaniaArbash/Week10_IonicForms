using System;
namespace DemoWeek1
{
    public class Calculator
    {

        public int add(int num1, int num2)
        {
            return num2 + num1;
        }

        public int sub(int num1, int num2)
        {
            return num2 - num1;
        }

        public int times(int num1, int num2)
        {
            return num2 * num1;
        }
    }
}
